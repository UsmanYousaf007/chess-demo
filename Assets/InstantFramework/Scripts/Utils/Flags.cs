        public static Sprite GetFlag(string countryId)
        {
            Sprite flag = null;

            if (countryId != null)
            {
                flag = Resources.Load<Sprite>("Flags/" + countryId);
            }

            if (flag == null)
            {
                flag = Resources.Load<Sprite>("Flags/_Unknown");
            }

            return flag;
        }

        public static string GetCountry(string countryCode)
        {
            if (countryList.ContainsKey(countryCode))
            {
                return countryList[countryCode];
            }

            return "Unknown";
        }
